    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (alunoClicado) {
            String loginAluno = (String) comboAluno.getSelectedItem();
            DefaultTableModel model = (DefaultTableModel) tabelaAlugueis.getModel();
            ArrayList resultados = aluguelController.listarAlugueisPorUsuario(loginAluno);
            model.setRowCount(0);
            for (int i = 0; i < resultados.size(); i++) {

                Aluguel l = (Aluguel) resultados.get(i);
                model.addRow(new Object[]{Integer.parseInt(l.getId()), l.getLogin(), l.getIdLivro(), l.getDataInicial(), l.getDataDeEntrega()});
            }
        } else if (professorClicado) {
            String loginProfessor = (String) comboProfessor.getSelectedItem();
            DefaultTableModel model = (DefaultTableModel) tabelaAlugueis.getModel();
            ArrayList resultados = aluguelController.listarAlugueisPorUsuario(loginProfessor);
            model.setRowCount(0);
            for (int i = 0; i < resultados.size(); i++) {

                Aluguel l = (Aluguel) resultados.get(i);
                model.addRow(new Object[]{Integer.parseInt(l.getId()), l.getLogin(), l.getIdLivro(), l.getDataInicial(), l.getDataDeEntrega()});
            }
        } else{
            JOptionPane.showMessageDialog(rootPane, "Por favor selecione um aluno ou professor.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

