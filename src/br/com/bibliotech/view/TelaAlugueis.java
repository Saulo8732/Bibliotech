    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabelaAlugueis.getModel();
        if (model.getRowCount() == 0) {
            ArrayList resultados = aluguelController.listarAlugueis();
            for (int i = 0; i < resultados.size(); i++) {
                Aluguel l = (Aluguel) resultados.get(i);
                model.addRow(new Object[]{Integer.parseInt(l.getId()), l.getLogin(), l.getIdLivro(), l.getDataInicial(), l.getDataDeEntrega()});

            }
        }
        AlunoController alunoController = new AlunoController();
        
        ArrayList alunosDisponiveis = alunoController.listarAlunos();
        comboAluno.removeAllItems();
        for(int i = 0; i < alunosDisponiveis.size(); i++){
            Aluno aluno = (Aluno) alunosDisponiveis.get(i);
            String nome = aluno.getLogin();
            comboAluno.addItem(nome);
        }
        
        ProfessorController professorController = new ProfessorController();
        
        ArrayList professoresDisponiveis = professorController.listarProfessors();
        comboProfessor.removeAllItems();
        for(int i = 0; i < professoresDisponiveis.size(); i++){
            Professor professor = (Professor) professoresDisponiveis.get(i);
            String nome = professor.getLogin();
            comboProfessor.addItem(nome);
        }
        
        
        
    }//GEN-LAST:event_formWindowActivated

