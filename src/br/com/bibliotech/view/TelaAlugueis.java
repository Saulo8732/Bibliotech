    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabelaAlugueis.getModel();
        if (tabelaAlugueis.getSelectedRow() >= 0) {
            String id = Integer.toString((Integer) tabelaAlugueis.getValueAt(linhaSelecionadaAnteriormente, 0));

            aluguelController.deletarAluguel(id);

            int a = tabelaAlugueis.getSelectedRow();
            model.removeRow(a);
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Por favor selecione um livro a ser devolvido.");
            System.out.println("Erro ao excluir");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

