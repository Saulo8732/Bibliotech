    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        ArrayList resultados = livroController.listarLivros();
        
        model.setRowCount(0);
        
        for (int i = 0; i < resultados.size(); i++){
            
            Livro l = (Livro) resultados.get(i);
            model.addRow(new Object[]{Integer.parseInt(l.getId()), l.getNome(), l.getDescricao(), l.getCategoria(), Integer.parseInt(l.getQuantidade())});
            
        }
    }//GEN-LAST:event_btnListarActionPerformed

