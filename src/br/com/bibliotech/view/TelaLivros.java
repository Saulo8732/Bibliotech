    private void jTable1PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jTable1PropertyChange
        // TODO add your handling code here:
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            if (linhaSelecionadaAnteriormente != -1 && colunaSelecionadaAnteriormente != -1) {
                String valor = "";
                if (colunaSelecionadaAnteriormente != 4){
                    valor = (String) jTable1.getValueAt(linhaSelecionadaAnteriormente, colunaSelecionadaAnteriormente);
                } else {
                    valor = Integer.toString((Integer) jTable1.getValueAt(linhaSelecionadaAnteriormente, colunaSelecionadaAnteriormente));
                }
                
                
                
                String indice = Integer.toString(colunaSelecionadaAnteriormente);
                String id = Integer.toString((Integer) jTable1.getValueAt(linhaSelecionadaAnteriormente, 0));
                
                livroController.alterarLivro(id, valor, indice);
                
            }
        } catch (Exception e) {
            System.out.println(e);
        }


    }//GEN-LAST:event_jTable1PropertyChange

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRow() >= 0) {
            String id = Integer.toString((Integer) jTable1.getValueAt(linhaSelecionadaAnteriormente, 0));

            livroController.deletarLivro(id);

            int a = jTable1.getSelectedRow();
            model.removeRow(a);
        }
        else {
            
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

