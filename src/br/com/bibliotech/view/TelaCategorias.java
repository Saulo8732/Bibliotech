    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        DefaultTableModel model = (DefaultTableModel) tabelaCategorias.getModel();
        ArrayList resultados = categoriaController.listarCategorias();
        
        model.setRowCount(0);
        
        for (int i = 0; i < resultados.size(); i++){
            
            Categoria l = (Categoria) resultados.get(i);
            model.addRow(new Object[]{Integer.parseInt(l.getId()), l.getNome(), l.getDescricao()});
            
        }
    }//GEN-LAST:event_btnListarActionPerformed

