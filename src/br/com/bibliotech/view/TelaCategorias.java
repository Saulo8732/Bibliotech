    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) tabelaCategorias.getModel();
        System.out.println("Muita loucura");
        if (model.getRowCount() == 0) {
            ArrayList resultados = categoriaController.listarCategorias();
            for (int i = 0; i < resultados.size(); i++) {
                Categoria l = (Categoria) resultados.get(i);
                model.addRow(new Object[]{Integer.parseInt(l.getId()), l.getNome(), l.getDescricao()});

            }
        }
        
    }//GEN-LAST:event_formWindowActivated

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabelaCategorias.getModel();
        if (tabelaCategorias.getSelectedRow() >= 0) {
            String id = Integer.toString((Integer) tabelaCategorias.getValueAt(linhaSelecionadaAnteriormente, 0));
            categoriaController.deletarCategoria(id);
            int a = tabelaCategorias.getSelectedRow();
            model.removeRow(a);
        }
        else {
            
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

